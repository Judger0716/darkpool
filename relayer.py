def secret_int_to_points(secret,threshold,num):
	coefficients = [secret]
	for i in range(threshold-1):
		# Take GF(2^32) as an example
		coefficients.append(randint(0,2**32-1))
	points = []
	for x_value in range(1,num+1):
		cur_point = [x_value]
		y_value = 0
		for i in range(len(coefficients)):
			y_value += coefficients[i]*pow(x_value,i)
		cur_point.append(y_value)
		points.append(cur_point)
	#print(points)
	return points

def parse_block(block_header, coin_type):


	curtime = time.time()
	curtime = curtime.__trunc__()
	block_template = {
		'version': '00000000',
		'type':  0,
		'hashBlock': '',
		'hashPrevBlock': '',
		'hashMerkleRoot': block_header['merkleroot'],
		'time': curtime,
		'bits': 0,
		'nonce': 0,
		'height': block_header['block_no'],
		'score': 0,
		'relayer': nodes[random.randint(0,len(nodes)-1)],
	}



	if coin_type == 'btc':

		block_template['type'] = 0 
		block_template['size'] = block_header['size']*8
		block_template['hashBlock'] = block_header['blockhash']
		block_template['hashPrevBlock'] = block_header['previous_blockhash']

	elif coin_type == 'eth':

		block_template['type'] = 1
		block_template['nonce'] = block_header['nonce']
		block_template['size'] = block_header['size']*8
		block_template['hashBlock'] = block_header['blockhash']
		block_template['hashPrevBlock'] = block_header['previous_blockhash']

	elif coin_type == 'ltc':

		block_template['type'] = 2
		block_template['size'] = block_header['size']*8
		block_template['hashBlock'] = '0x' + block_header['blockhash']
		block_template['hashPrevBlock'] = '0x' + block_header['previous_blockhash']

	elif coin_type == 'atom':

		block_template['type'] = 3
		block_template['hashBlock'] = '0x' + block_header['blockhash'].lower()
		block_template['hashPrevBlock'] = '0x' + block_header['previous_blockhash'].lower()

	elif coin_type == 'dcr':

		block_template['type'] = 4
		block_template['size'] = block_header['size']*8
		block_template['hashBlock'] = block_header['blockhash']
		block_template['hashPrevBlock'] = block_header['previous_blockhash']

	elif coin_type == 'doge':

		block_template['type'] = 5
		block_template['size'] = block_header['size']*8
		block_template['hashBlock'] = '0x' + block_header['blockhash']
		block_template['hashPrevBlock'] = '0x' + block_header['previous_blockhash']

	return block_template





